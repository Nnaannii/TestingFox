ALTER    FUNCTION dbo.fn_isHUNo
(
@HUNo varchar(50)
)
RETURNS BIT
BEGIN
	DECLARE @_return BIT
	IF EXISTS(SELECT 0 from mfhulabelstatus (NOLOCK) WHERE HU_Id=@HUNo) 
		SET @_return='1'
	ELSE
		SET @_return='0'
RETURN @_return
END




